# Conversão de tipos, é chamado de coerção ou Type conversion, typecasting, coercion
# É o ato de converter um tipo em outro 
# Tipo imutáveis e primitivos: 
# str, int, float, bool
# Sinal de + com números é soma, com strings é concatenação 
# Usando a class int podemos converter

print(int('1'),type(int('1')))
print(int('1') + 1)
print(float('1.1') + 1)
print(bool(' '))    # uma string bool vazia é considerada falsa, com alguma coisa é verdadeira 
print(str(11)+'b')