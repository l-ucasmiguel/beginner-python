# Tipo de dado bool (boolean)

# Ao questionar algo em um programa, só existe duas respostas possíveis, verdadeiro ou falso, 0 ou 1 
# Existe vários operadores para questionar, como o ==, que é um operador lógico que questiona se um valor é igual o outro 

print(10 == 10)     # -> True
print(10 == '10')   # -> False
print(type(10==10)) # -> bool