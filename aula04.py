# Tipos int e float

# INT -> números inteiros
# O tipo INT representa qualquer número postivo ou negativo, int sem sinal é considerado positivo
print(11)   # int
print(-11)  # int

# FLOAT -> número com ponto flutuante 
# O tipo FLOAT representa qualquer número que positivo ou negativo com número flutuante, float sem sinal é considerado positivo
print(1.1, 10.11)
print(0.0, -2.1)

# A função type mostra o tipo que o python inferiu o valor 
print(type(2.0))
print(type('qwerty'))
print(type('qwerty'), type(1), type(2.0))