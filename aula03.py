"""
Python = Linguagem de programação
Tipo de tipagem  = Dinâmica / Forte
str -> string -> texto
strings são textos que estão dentro das aspas 
"""

# Aspas simples ' '
print('Renato Augusto')

# Aspas duplas  " "
print("Renato Augusto")

# Escape        \
print("Renato \"Augusto\"")

# r
print(r"Renato \"Augusto\"")

#Truque Aspas simples dentro de duplas ou vice versa funciona
print('"Renato Augusto"')
print("'Renato Augusto'")